#!/usr/bin/env python3
"""
Batch reprocess all decisions from backup through the improved AI pipeline.

Reads decision records from a backup JSON file, runs each through the AI
processor + post-processor + inline fixes, and inserts clean results into DB.

Supports resume via checkpoint file — safe to interrupt and restart.

Usage:
    # Test with 5 records, no DB writes
    python bin/batch_reprocess.py --limit 5 --dry-run --verbose

    # Test with 5 actual inserts
    python bin/batch_reprocess.py --limit 5 --verbose

    # Full run: delete all DB records, reprocess from backup
    python bin/batch_reprocess.py --delete-first --verbose

    # Resume after interruption
    python bin/batch_reprocess.py --resume --verbose
"""

import argparse
import json
import logging
import os
import sys
import time
from datetime import datetime
from pathlib import Path

# Setup path
sys.path.insert(0, os.path.abspath(os.path.join(os.path.dirname(__file__), '..')))

from dotenv import load_dotenv
load_dotenv()

from src.gov_scraper.db.connector import get_supabase_client
from src.gov_scraper.processors.ai import process_decision_with_ai
from src.gov_scraper.processors.ai_post_processor import post_process_ai_results
from src.gov_scraper.processors.qa import apply_inline_fixes

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s [%(levelname)s] %(message)s',
    datefmt='%H:%M:%S'
)
logger = logging.getLogger(__name__)

# Fields to preserve from backup (structural, not AI-generated)
STRUCTURAL_FIELDS = [
    'decision_key',
    'decision_number',
    'decision_date',
    'decision_title',
    'decision_content',
    'decision_url',
    'committee',
    'government_number',
    'prime_minister',
]

# Fields generated by AI pipeline
AI_FIELDS = [
    'summary',
    'operativity',
    'tags_policy_area',
    'tags_government_body',
    'tags_location',
    'all_tags',
]

DEFAULT_BACKUP = 'backups/pre_deployment_20260218_143933.json'
CHECKPOINT_FILE = 'data/reprocess_checkpoint.json'


def load_backup(backup_path: str) -> list:
    """Load records from backup JSON file."""
    logger.info(f"Loading backup from {backup_path}...")
    with open(backup_path, 'r', encoding='utf-8') as f:
        records = json.load(f)
    logger.info(f"Loaded {len(records)} records from backup")
    return records


def load_checkpoint() -> dict:
    """Load checkpoint file if it exists."""
    if os.path.exists(CHECKPOINT_FILE):
        with open(CHECKPOINT_FILE, 'r', encoding='utf-8') as f:
            return json.load(f)
    return {
        'processed_keys': [],
        'last_updated': None,
        'total_processed': 0,
        'total_errors': 0,
        'errors': []
    }


def save_checkpoint(checkpoint: dict):
    """Save checkpoint to file."""
    os.makedirs(os.path.dirname(CHECKPOINT_FILE), exist_ok=True)
    checkpoint['last_updated'] = datetime.now().isoformat()
    with open(CHECKPOINT_FILE, 'w', encoding='utf-8') as f:
        json.dump(checkpoint, f, ensure_ascii=False, indent=2)


def delete_all_records():
    """Delete all records from the decisions table in batches to avoid timeout."""
    client = get_supabase_client()
    logger.warning("DELETING ALL RECORDS from israeli_government_decisions (in batches)...")

    total_deleted = 0
    batch_num = 0

    while True:
        batch_num += 1
        # Fetch a batch of keys to delete
        response = client.table("israeli_government_decisions") \
            .select("decision_key") \
            .limit(500) \
            .execute()

        if not response.data:
            break

        keys = [r['decision_key'] for r in response.data]
        logger.info(f"Delete batch {batch_num}: removing {len(keys)} records...")

        # Delete this batch
        client.table("israeli_government_decisions") \
            .delete() \
            .in_('decision_key', keys) \
            .execute()

        total_deleted += len(keys)
        logger.info(f"Deleted so far: {total_deleted}")

        # Brief pause to avoid overloading
        time.sleep(0.5)

    logger.info(f"Deletion complete: {total_deleted} total records removed")
    return total_deleted


def process_single_record(record: dict, verbose: bool = False) -> dict:
    """
    Process a single backup record through the AI pipeline.

    Returns a clean decision dict ready for DB insertion, or None on error.
    """
    decision_key = record.get('decision_key', '?')
    decision_content = record.get('decision_content', '')

    if not decision_content or not decision_content.strip():
        logger.warning(f"[{decision_key}] No content — skipping AI, preserving existing fields")
        # Still include structural + existing AI fields as-is
        result = {}
        for field in STRUCTURAL_FIELDS + AI_FIELDS:
            val = record.get(field)
            if val is not None:
                result[field] = val
        return result

    # Build input for AI processor
    decision_data = {}
    for field in STRUCTURAL_FIELDS:
        val = record.get(field)
        if val is not None:
            decision_data[field] = val

    # Run AI pipeline
    try:
        processed = process_decision_with_ai(decision_data, use_unified=True)
    except Exception as e:
        logger.error(f"[{decision_key}] AI processing failed: {e}")
        # Try legacy fallback
        try:
            logger.info(f"[{decision_key}] Trying legacy AI...")
            processed = process_decision_with_ai(decision_data, use_unified=False)
        except Exception as e2:
            logger.error(f"[{decision_key}] Legacy AI also failed: {e2}")
            return None

    # Apply inline fixes (operativity typos, hallucination removal, all_tags rebuild)
    processed = apply_inline_fixes(processed)

    # Post-processor is already called inside process_decision_with_ai for unified mode,
    # but apply it again for safety (idempotent operations)
    processed = post_process_ai_results(processed, decision_content)

    # Build final clean record: structural from backup + AI from pipeline
    final = {}
    for field in STRUCTURAL_FIELDS:
        val = record.get(field)
        if val is not None:
            final[field] = val

    for field in AI_FIELDS:
        val = processed.get(field)
        if val is not None:
            final[field] = val

    # Remove any internal metadata fields (start with _)
    final = {k: v for k, v in final.items() if not k.startswith('_')}

    if verbose:
        logger.info(f"[{decision_key}] summary={final.get('summary', '')[:60]}...")
        logger.info(f"[{decision_key}] tags={final.get('tags_policy_area', '')}")
        logger.info(f"[{decision_key}] bodies={final.get('tags_government_body', '')}")
        logger.info(f"[{decision_key}] operativity={final.get('operativity', '')}")

    return final


def insert_batch(records: list, dry_run: bool = False) -> tuple:
    """Insert a batch of records into DB. Returns (success_count, errors)."""
    if dry_run:
        logger.info(f"[DRY RUN] Would insert {len(records)} records")
        return len(records), []

    client = get_supabase_client()
    inserted = 0
    errors = []

    # Clean records: remove None values
    clean_records = [{k: v for k, v in r.items() if v is not None} for r in records]

    try:
        client.table("israeli_government_decisions").insert(clean_records).execute()
        inserted = len(clean_records)
    except Exception as e:
        error_str = str(e).lower()
        if 'unique constraint' in error_str or 'duplicate key' in error_str:
            logger.warning(f"Batch had duplicates, falling back to individual inserts")
            for record in clean_records:
                key = record.get('decision_key', '?')
                try:
                    client.table("israeli_government_decisions").insert(record).execute()
                    inserted += 1
                except Exception as e2:
                    if 'unique constraint' in str(e2).lower() or 'duplicate key' in str(e2).lower():
                        logger.info(f"[{key}] Already exists — skipping")
                        inserted += 1  # Count as success (already there)
                    else:
                        logger.error(f"[{key}] Insert failed: {e2}")
                        errors.append(f"{key}: {e2}")
        else:
            logger.error(f"Batch insert failed: {e}")
            errors.append(str(e))

    return inserted, errors


def main():
    parser = argparse.ArgumentParser(description='Batch reprocess decisions from backup through AI pipeline')
    parser.add_argument('--backup', default=DEFAULT_BACKUP, help='Path to backup JSON file')
    parser.add_argument('--delete-first', action='store_true', help='Delete all DB records before starting')
    parser.add_argument('--batch-size', type=int, default=25, help='Insert batch size (default: 25)')
    parser.add_argument('--limit', type=int, help='Process only first N records')
    parser.add_argument('--resume', action='store_true', help='Resume from checkpoint file')
    parser.add_argument('--dry-run', action='store_true', help='Process but don\'t write to DB')
    parser.add_argument('--verbose', action='store_true', help='Show per-record details')
    parser.add_argument('--no-approval', action='store_true', help='Skip confirmation prompts')
    args = parser.parse_args()

    # Load backup
    records = load_backup(args.backup)
    if not records:
        logger.error("No records in backup file!")
        sys.exit(1)

    # Apply limit
    if args.limit:
        records = records[:args.limit]
        logger.info(f"Limited to {args.limit} records")

    # Load or reset checkpoint
    if args.resume:
        checkpoint = load_checkpoint()
        processed_keys = set(checkpoint['processed_keys'])
        logger.info(f"Resuming: {len(processed_keys)} already processed, skipping them")
    else:
        checkpoint = load_checkpoint() if not args.delete_first else {
            'processed_keys': [],
            'last_updated': None,
            'total_processed': 0,
            'total_errors': 0,
            'errors': []
        }
        processed_keys = set(checkpoint['processed_keys'])

    # Delete all records if requested
    if args.delete_first and not args.dry_run:
        if not args.no_approval:
            confirm = input(f"\n⚠️  This will DELETE ALL records from the database.\n"
                          f"Backup has {len(records)} records to reprocess.\n"
                          f"Type 'DELETE' to confirm: ")
            if confirm != 'DELETE':
                print("Aborted.")
                sys.exit(0)
        delete_all_records()
        # Reset checkpoint after delete
        checkpoint = {
            'processed_keys': [],
            'last_updated': None,
            'total_processed': 0,
            'total_errors': 0,
            'errors': []
        }
        processed_keys = set()
        save_checkpoint(checkpoint)

    # Process records
    total = len(records)
    batch = []
    batch_keys = []
    start_time = time.time()
    success_count = 0
    error_count = 0
    skip_count = 0

    print(f"\n{'='*70}")
    print(f"  BATCH REPROCESS: {total} records")
    print(f"  Mode: {'DRY RUN' if args.dry_run else 'LIVE'}")
    print(f"  Batch size: {args.batch_size}")
    print(f"  Resume: {len(processed_keys)} already done")
    print(f"{'='*70}\n")

    for i, record in enumerate(records):
        decision_key = record.get('decision_key', '?')

        # Skip already processed
        if decision_key in processed_keys:
            skip_count += 1
            continue

        # Process through AI pipeline
        try:
            result = process_single_record(record, verbose=args.verbose)
        except Exception as e:
            logger.error(f"[{decision_key}] Unexpected error: {e}")
            result = None

        if result is None:
            error_count += 1
            checkpoint['errors'].append({
                'key': decision_key,
                'error': 'AI processing failed',
                'timestamp': datetime.now().isoformat()
            })
            continue

        batch.append(result)
        batch_keys.append(decision_key)

        # Insert when batch is full
        if len(batch) >= args.batch_size:
            inserted, errors = insert_batch(batch, dry_run=args.dry_run)
            success_count += inserted

            # Update checkpoint
            checkpoint['processed_keys'].extend(batch_keys)
            processed_keys.update(batch_keys)
            checkpoint['total_processed'] = len(processed_keys)
            checkpoint['total_errors'] = error_count
            save_checkpoint(checkpoint)

            if errors:
                error_count += len(errors)
                for err in errors:
                    checkpoint['errors'].append({
                        'key': '?',
                        'error': err,
                        'timestamp': datetime.now().isoformat()
                    })

            batch = []
            batch_keys = []

        # Progress report
        processed_so_far = success_count + error_count + skip_count + len(batch)
        if (i + 1) % 50 == 0 or (i + 1) == total:
            elapsed = time.time() - start_time
            rate = (success_count + error_count) / elapsed if elapsed > 0 else 0
            remaining = (total - processed_so_far) / rate if rate > 0 else 0
            logger.info(
                f"Progress: {processed_so_far}/{total} "
                f"({success_count} ok, {error_count} err, {skip_count} skip) "
                f"| {rate:.1f}/s | ~{remaining/60:.0f}m remaining"
            )

    # Insert remaining batch
    if batch:
        inserted, errors = insert_batch(batch, dry_run=args.dry_run)
        success_count += inserted
        checkpoint['processed_keys'].extend(batch_keys)
        checkpoint['total_processed'] = len(checkpoint['processed_keys'])
        checkpoint['total_errors'] = error_count
        save_checkpoint(checkpoint)

    # Final report
    elapsed = time.time() - start_time
    print(f"\n{'='*70}")
    print(f"  REPROCESS COMPLETE")
    print(f"{'='*70}")
    print(f"  Total records:    {total}")
    print(f"  Processed:        {success_count}")
    print(f"  Errors:           {error_count}")
    print(f"  Skipped (resume): {skip_count}")
    print(f"  Time:             {elapsed/60:.1f} minutes")
    print(f"  Rate:             {(success_count + error_count) / elapsed:.1f} records/sec" if elapsed > 0 else "")
    print(f"  Checkpoint:       {CHECKPOINT_FILE}")
    print(f"{'='*70}\n")

    if error_count > 0:
        print(f"⚠️  {error_count} errors occurred. Check checkpoint file for details.")
        sys.exit(1)
    else:
        print("✅ All records processed successfully!")


if __name__ == '__main__':
    main()
