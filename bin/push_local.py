class ValidationResult:
    """Container for validation results"""
    def __init__(self, record: Dict, is_valid: bool, errors: List[str], warnings: List[str], fixes_applied: List[str]):
        self.record = record
        self.is_valid = is_valid
        self.errors = errors
        self.warnings = warnings
        self.fixes_applied = fixes_applied
        self.decision_key = record.get('decision_key', 'unknown')


class PushLocalProcessor:
    """Main processor for local JSON QA and push operations"""

    def __init__(self, verbose: bool = False):
        self.verbose = verbose
        self.stats = {
            'total_processed': 0,
            'passed_validation': 0,
            'failed_validation': 0,
            'fixes_applied': 0,
            'db_inserted': 0,
            'db_errors': 0
        }
        self.failed_records = []

    def validate_date(self, date_str: str) -> Tuple[bool, List[str]]:
        """Validate date format and range"""
        errors = []
        if not date_str:
            errors.append("Missing decision date")
            return False, errors

        # Check format (YYYY-MM-DD)
        if not re.match(r'^\d{4}-\d{2}-\d{2}$', date_str):
            errors.append(f"Invalid date format: {date_str} (expected YYYY-MM-DD)")
            return False, errors

        # Check range (1948-2027)
        try:
            year = int(date_str.split('-')[0])
            if year < 1948 or year > 2027:
                errors.append(f"Date year {year} out of valid range (1948-2027)")
                return False, errors
        except (ValueError, IndexError):
            errors.append(f"Cannot parse year from date: {date_str}")
            return False, errors

        return True, []

    def validate_decision_key_format(self, decision_key: str, government_number: str, decision_number: str) -> Tuple[bool, List[str]]:
        """Validate decision key format"""
        errors = []
        expected_key = f"{government_number}_{decision_number}"

        if decision_key != expected_key:
            errors.append(f"Decision key mismatch: got '{decision_key}', expected '{expected_key}'")
            return False, errors

        # Validate government number
        try:
            gov_num = int(government_number)
            if gov_num < 1 or gov_num > 50:  # Reasonable range
                errors.append(f"Government number {gov_num} seems invalid")
        except ValueError:
            errors.append(f"Government number is not numeric: {government_number}")
            return False, errors

        return True, []

    def validate_operativity(self, operativity: str) -> Tuple[bool, List[str]]:
        """Validate operativity field"""
        errors = []
        valid_values = ["אופרטיבית", "דקלרטיבית"]

        if operativity not in valid_values:
            errors.append(f"Invalid operativity value: '{operativity}' (must be one of {valid_values})")
            return False, errors

        return True, []

    def validate_content_length(self, content: str) -> Tuple[bool, List[str]]:
        """Validate content length"""
        warnings = []
        if len(content) < 100:
            warnings.append(f"Content is very short: {len(content)} characters")
            return True, warnings  # Warning, not error

        return True, []

    def validate_single_record(self, record: Dict) -> ValidationResult:
        """Validate a single record with comprehensive QA checks"""
        errors = []
        warnings = []
        fixes_applied = []

        # Create a copy to avoid modifying original
        validated_record = record.copy()

        # 1. Basic field validation using existing function
        basic_errors = validate_decision_data(validated_record)
        errors.extend(basic_errors)

        # 2. Date validation
        if 'decision_date' in validated_record:
            date_valid, date_errors = self.validate_date(validated_record['decision_date'])
            errors.extend(date_errors)

        # 3. Decision key validation
        if all(k in validated_record for k in ['decision_key', 'government_number', 'decision_number']):
            key_valid, key_errors = self.validate_decision_key_format(
                validated_record['decision_key'],
                str(validated_record['government_number']),
                str(validated_record['decision_number'])
            )
            errors.extend(key_errors)

        # 4. Operativity validation
        if 'operativity' in validated_record:
            op_valid, op_errors = self.validate_operativity(validated_record['operativity'])
            errors.extend(op_errors)

        # 5. Content length check (warning only)
        if 'decision_content' in validated_record:
            content_valid, content_warnings = self.validate_content_length(validated_record['decision_content'])
            warnings.extend(content_warnings)

        # 6. Validate post-processing was already applied (do NOT re-apply —
        #    post_process_ai_results() is called during Phase B in the pipeline.
        #    Re-applying could mutate tags differently if lists changed.)
        try:
            fixes = []
            # Check if post-processing looks already applied
            summary = validated_record.get('summary', '')
            if summary and summary.startswith('החלטת ממשלה'):
                warnings.append("Summary still has forbidden prefix — post-processing may not have run")

            policy_tags = validated_record.get('tags_policy_area', '')
            if policy_tags:
                from src.gov_scraper.processors.ai_post_processor import AUTHORIZED_POLICY_AREAS
                tags = [t.strip() for t in policy_tags.split(';') if t.strip()]
                unauthorized = [t for t in tags if t not in AUTHORIZED_POLICY_AREAS]
                if unauthorized:
                    warnings.append(f"Unauthorized policy tags found: {unauthorized}")

            gov_bodies = validated_record.get('tags_government_body', '')
            if gov_bodies:
                from src.gov_scraper.processors.ai_post_processor import AUTHORIZED_GOVERNMENT_BODIES
                bodies = [b.strip() for b in gov_bodies.split(';') if b.strip()]
                unauthorized_bodies = [b for b in bodies if b not in AUTHORIZED_GOVERNMENT_BODIES]
                if unauthorized_bodies:
                    warnings.append(f"Unauthorized gov bodies found: {unauthorized_bodies}")
            if record.get('all_tags') != validated_record.get('all_tags'):
                fixes.append("all_tags rebuilt deterministically")

            fixes_applied.extend(fixes)
            if fixes:
                self.stats['fixes_applied'] += 1

        except Exception as e:
            errors.append(f"Post-processing failed: {str(e)}")

        # 7. Additional manual checks

        # Check for required decision_key
        if not validated_record.get('decision_key'):
            try:
                # Try to generate it
                validated_record['decision_key'] = generate_decision_key(validated_record)
                fixes_applied.append("decision_key generated")
            except Exception as e:
                errors.append(f"Cannot generate decision_key: {str(e)}")

        # Check for empty tags after whitelist enforcement
        if not validated_record.get('tags_policy_area'):
            warnings.append("No policy area tags after validation")

        is_valid = len(errors) == 0

        return ValidationResult(
            record=validated_record,
            is_valid=is_valid,
            errors=errors,
            warnings=warnings,
            fixes_applied=fixes_applied
        )

    def load_json_file(self, file_path: str) -> List[Dict]:
        """Load and parse JSON file"""
        logger.info(f"Loading JSON file: {file_path}")

        if not os.path.exists(file_path):
            raise FileNotFoundError(f"File not found: {file_path}")

        try:
            with open(file_path, 'r', encoding='utf-8') as f:
                data = json.load(f)

            if not isinstance(data, list):
                raise ValueError("JSON file must contain a list of records")

            logger.info(f"Loaded {len(data)} records from {file_path}")
            return data

        except json.JSONDecodeError as e:
            raise ValueError(f"Invalid JSON format: {str(e)}")
        except Exception as e:
            raise RuntimeError(f"Error loading file: {str(e)}")

    def save_failed_records(self, output_dir: str = "data") -> str:
        """Save failed records to JSON file"""
        if not self.failed_records:
            return None

        os.makedirs(output_dir, exist_ok=True)
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        filename = f"failed_qa_{timestamp}.json"
        filepath = os.path.join(output_dir, filename)

        # Convert ValidationResult objects to serializable format
        failed_data = []
        for result in self.failed_records:
            failed_data.append({
                'record': result.record,
                'errors': result.errors,
                'warnings': result.warnings,
                'fixes_applied': result.fixes_applied,
                'decision_key': result.decision_key
            })

        with open(filepath, 'w', encoding='utf-8') as f:
            json.dump(failed_data, f, ensure_ascii=False, indent=2)

        logger.info(f"Saved {len(self.failed_records)} failed records to {filepath}")
        return filepath

    def print_validation_report(self, results: List[ValidationResult]):
        """Print detailed validation report"""
        print("\n" + "="*60)
        print("VALIDATION REPORT")
        print("="*60)

        for i, result in enumerate(results, 1):
            print(f"\n[{i}] Decision: {result.decision_key}")
            print(f"    Status: {'✅ PASSED' if result.is_valid else '❌ FAILED'}")

            if result.fixes_applied:
                print(f"    Fixes Applied: {', '.join(result.fixes_applied)}")

            if result.warnings:
                print(f"    Warnings: {'; '.join(result.warnings)}")

            if result.errors:
                print(f"    Errors: {'; '.join(result.errors)}")

            if self.verbose and result.is_valid:
                # Show key fields for passed records
                record = result.record
                print(f"    Title: {record.get('decision_title', 'N/A')[:50]}...")
                print(f"    Policy Tags: {record.get('tags_policy_area', 'N/A')}")
                print(f"    Gov Bodies: {record.get('tags_government_body', 'N/A')}")
                print(f"    Operativity: {record.get('operativity', 'N/A')}")

    def print_summary_statistics(self):
        """Print summary statistics"""
        print("\n" + "="*60)
        print("SUMMARY STATISTICS")
        print("="*60)
        print(f"Total Processed:     {self.stats['total_processed']}")
        print(f"Passed Validation:   {self.stats['passed_validation']} ({self.stats['passed_validation']/max(self.stats['total_processed'],1)*100:.1f}%)")
        print(f"Failed Validation:   {self.stats['failed_validation']} ({self.stats['failed_validation']/max(self.stats['total_processed'],1)*100:.1f}%)")
        print(f"Records Fixed:       {self.stats['fixes_applied']}")
        print(f"DB Inserted:         {self.stats['db_inserted']}")
        print(f"DB Errors:           {self.stats['db_errors']}")

        if self.stats['failed_validation'] > 0:
            print(f"\nFailed records saved to file for review.")

    def push_to_database(self, valid_records: List[Dict]) -> Tuple[int, int]:
        """Push valid records to database"""
        if not valid_records:
            logger.info("No valid records to insert")
            return 0, 0

        logger.info(f"Inserting {len(valid_records)} records to database...")

        try:
            # Test database connection first
            client = get_supabase_client()
            logger.info("Database connection successful")

            # Insert records using existing DAL function
            inserted_count, error_keys = insert_decisions_batch(valid_records, batch_size=50)

            logger.info(f"Inserted {inserted_count} records successfully")
            if error_keys:
                logger.warning(f"{len(error_keys)} records failed to insert: {error_keys[:5]}...")

            return inserted_count, len(error_keys)

        except Exception as e:
            logger.error(f"Database insertion failed: {str(e)}")
            return 0, len(valid_records)

    def process_file(self, file_path: str, qa_only: bool = False, push_to_db: bool = False) -> bool:
        """Main processing function"""
        try:
            # Load JSON data
            records = self.load_json_file(file_path)
            self.stats['total_processed'] = len(records)

            # Validate each record
            logger.info("Starting QA validation...")
            results = []
            valid_records = []

            for record in records:
                result = self.validate_single_record(record)
                results.append(result)

                if result.is_valid:
                    self.stats['passed_validation'] += 1
                    valid_records.append(result.record)
                else:
                    self.stats['failed_validation'] += 1
                    self.failed_records.append(result)

            # Print validation report
            self.print_validation_report(results)

            # Save failed records if any
            if self.failed_records:
                self.save_failed_records()

            # Push to database if requested
            if push_to_db and not qa_only:
                if valid_records:
                    inserted, errors = self.push_to_database(valid_records)
                    self.stats['db_inserted'] = inserted
                    self.stats['db_errors'] = errors
                else:
                    logger.warning("No valid records to push to database")
            elif qa_only:
                logger.info("QA-only mode: skipping database insertion")

            # Print summary
            self.print_summary_statistics()

            return self.stats['failed_validation'] == 0

        except Exception as e:
            logger.error(f"Processing failed: {str(e)}")
            return False


def main():
    """Main entry point"""
    parser = argparse.ArgumentParser(
        description="Phase 3 QA validation and DB push for local JSON files",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  %(prog)s --file data/scraped_decisions.json --qa-only
  %(prog)s --file data/scraped_decisions.json --push --verbose
  %(prog)s --file recent_decisions.json --qa-only --verbose
        """
    )

    parser.add_argument(
        '--file',
        required=True,
        help='Path to local JSON file to process'
    )

    parser.add_argument(
        '--qa-only',
        action='store_true',
        help='Validate and report only, no DB writes'
    )

    parser.add_argument(
        '--push',
        action='store_true',
        help='Validate then insert passing records to DB'
    )

    parser.add_argument(
        '--verbose',
        action='store_true',
        help='Detailed output during processing'
    )

    args = parser.parse_args()

    # Validate arguments
    if not args.qa_only and not args.push:
        parser.error("Must specify either --qa-only or --push")

    if args.qa_only and args.push:
        parser.error("Cannot specify both --qa-only and --push")

    # Set logging level based on verbosity
    if args.verbose:
        logging.getLogger().setLevel(logging.DEBUG)
        # Also set logger for the processors
        logging.getLogger('src.gov_scraper.processors.ai_post_processor').setLevel(logging.DEBUG)

    # Process the file
    processor = PushLocalProcessor(verbose=args.verbose)

    print(f"Processing file: {args.file}")
    print(f"Mode: {'QA-only' if args.qa_only else 'QA + DB Push'}")
    print(f"Verbose: {args.verbose}")
    print(f"Authorized Policy Areas: {len(AUTHORIZED_POLICY_AREAS)}")
    print(f"Authorized Government Bodies: {len(AUTHORIZED_GOVERNMENT_BODIES)}")

    success = processor.process_file(
        file_path=args.file,
        qa_only=args.qa_only,
        push_to_db=args.push
    )

    sys.exit(0 if success else 1)


if __name__ == '__main__':
    main()