def get_tags_by_ministry(ministry: str) -> List[str]:
    """Get tags that correlate with a specific ministry."""
    matching_tags = []
    for tag, profile in TAG_DETECTION_PROFILES.items():
        if ministry in profile.get("ministry_correlation", []):
            matching_tags.append(tag)
    return matching_tags

